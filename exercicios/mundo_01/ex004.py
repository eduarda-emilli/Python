y = (input('Digite qualquer coisa: '))

print('Você escreveu {}' .format(y))
print('Você escreveu o tipo', type(y))
print('Ele é númerico?', y.isnumeric())
print('Ele é alphanúmerico? ', y.isalnum())
print('Está escrito em minúsculo? ', y.islower())
print('É alfabético?', y.isalpha())
print('Está capitalizada?', y.istitle())





































